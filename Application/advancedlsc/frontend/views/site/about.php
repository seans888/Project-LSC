<h2> Outreach Programs </h2>
<hr style ="margin-left: 270px; margin-right: 1000px;">

<p>Every summer since 1999, LSC has served as the implementing partner 
of the Rotary Club of Loyola Heights in granting college entrance test 
review scholarships to selected fourth year students from poor families 
desiring to enter the top universities in Metro Manila. Several of these 
review scholars have not only made it to the universities of their 
choice but have even qualified for the scholarships offered by those 
schools. This is now the eleventh year that the Ford Foundation, 
through Ateneo’s Pathways to Higher Education Program, has chosen LSC 
to be its official college entrance test review center of the 120 
scholars that it selects yearly from depressed communities in Metro 
Manila. LSC has also extended its review programs to other students in
 need of financial assistance. </p>
 
