<h2> Mission </h2>
<hr style ="margin-left: 270px; margin-right: 1000px;">


<p>Our mission is to enhance the intellectual development of students 
in the Philippines by augmenting the learning they receive from the 
educational system. We aim to accomplish this by providing the most 
comprehensive and highest quality tutorials and review services, 
maintaining competent and progressive teaching and support staff, 
continually updating our books and materials, and providing an 
environment conducive to learning and achievement. </p>

<h2> Vision </h2>
<hr style ="margin-left: 270px; margin-right: 1000px;">

<p>We aspire to become a center of educational excellence and a 
resource for sectors of our society deprived of opportunities for 
advance learning. We desire to see the students we help achieve their 
educational goals – from the simplest one of passing their school 
quizzes to developing their character and critical thinking skills - 
so that they may become successful members of society and contribute 
to the development of the country. </p>


<h2> History </h2>
<hr style ="margin-left: 270px; margin-right: 1000px;">

<p>In the early 1980’s, a group of faculty members from the University 
of the Philippines and the Ateneo de Manila University got together to 
provide tutorial services in their spare time to children of faculty 
and staff of the two universities. As more and more students from these 
and other schools availed of their services, some of these educators 
opted to undertake tutoring full-time. One of them established, 
in 1986, a tutorial and review facility at a strategic location on 
Katipunan Avenue. This became known as the Solomon’s Ark Computer & 
Tutorial Services Center. After 5 years, its name was changed to the 
Loyola Student Center. From a handful of students that year, LSC’s 
clientele has increased considerably. Over the past 23 years, LSC has 
helped over 22, 000 students perform better in school. Today LSC takes 
leadership in providing high-quality and affordable tutorial and review 
services to students not only in Metro Manila but also from the 
provinces.</p>


